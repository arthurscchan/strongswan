static mem_cred_t *creds;

int LLVMFuzzerInitialize(int *argc, char ***argv)
{
	certificate_t *cert;
	private_key_t *key;

	dbg_default_set_level(-1);
	library_init(NULL, "fuzz_pkcs7");
	if (!lib->plugins->load(lib->plugins, PLUGINS))
	{
		return 1;
	}

	creds = mem_cred_create();
	lib->credmgr->add_set(lib->credmgr, &creds->set);

	cert = lib->creds->create(lib->creds, CRED_CERTIFICATE, CERT_X509,
							  BUILD_BLOB, fuzz_cert, BUILD_END);
	if (cert)
	{
		creds->add_cert(creds, TRUE, cert);
	}
	key = lib->creds->create(lib->creds, CRED_PRIVATE_KEY, KEY_ANY,
							 BUILD_BLOB, fuzz_key, BUILD_END);
	if (key)
	{
		creds->add_key(creds, key);
	}
	return 0;
}

int LLVMFuzzerTestOneInput(const uint8_t *buf, size_t len)
{
	container_t *container;
	pkcs7_t *pkcs7;
	enumerator_t *enumerator;
	certificate_t *cert;
	auth_cfg_t *auth;
	chunk_t chunk, data;

	chunk = chunk_create((u_char*)buf, len);
	container = lib->creds->create(lib->creds, CRED_CONTAINER, CONTAINER_PKCS7,
								   BUILD_BLOB, chunk, BUILD_END);
	if (container)
	{
		container->get_type(container);

		pkcs7 = (pkcs7_t*)container;
		enumerator = pkcs7->create_cert_enumerator(pkcs7);
		while (enumerator->enumerate(enumerator, &cert)) { }
		enumerator->destroy(enumerator);

		/* Verification against the trusted cert installed in init. */
		enumerator = container->create_signature_enumerator(container);
		while (enumerator->enumerate(enumerator, &auth)) { }
		enumerator->destroy(enumerator);

		/* Decryption path for EnvelopedData */
		if (container->get_data(container, &data))
		{
			chunk_free(&data);
		}

		container->destroy(container);
	}
	return 0;
}

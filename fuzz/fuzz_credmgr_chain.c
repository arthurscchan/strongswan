#define MAX_INTERMEDIATES 2

static mem_cred_t *anchor_creds;
static certificate_t *anchor_cert;
static cert_validator_t *constraints_v;
static cert_validator_t *revocation_v;
static constraints_validator_t *constraints_inst;
static revocation_validator_t *revocation_inst;

int LLVMFuzzerInitialize(int *argc, char ***argv)
{
	certificate_t *ca, *crl, *ocsp;

	dbg_default_set_level(-1);
	library_init(NULL, "fuzz_credmgr_chain");

	/* PLUGINS must include revocation + constraints to register the validators. */
	if (!lib->plugins->load(lib->plugins, PLUGINS))
	{
		return 1;
	}

	anchor_creds = mem_cred_create();

	/* Trust anchor with constraint/policy/EKU extensions. */
	ca = lib->creds->create(lib->creds, CRED_CERTIFICATE, CERT_X509,
							 BUILD_BLOB, ca_cert, BUILD_END);
	if (!ca)
	{
		return 1;
	}
	anchor_cert = ca->get_ref(ca);
	anchor_creds->add_cert(anchor_creds, TRUE, ca);

	/* Pre-load CRL so revocation_validator's CRL-lookup path is exercised. */
	crl = lib->creds->create(lib->creds, CRED_CERTIFICATE, CERT_X509_CRL,
							  BUILD_BLOB, ca_crl, BUILD_END);
	if (crl)
	{
		anchor_creds->add_crl(anchor_creds, (crl_t*)crl);
	}

	/* Pre-load OCSP response */
	ocsp = lib->creds->create(lib->creds, CRED_CERTIFICATE,
							   CERT_X509_OCSP_RESPONSE, BUILD_BLOB,
							   ca_ocsp, BUILD_END);
	if (ocsp)
	{
		anchor_creds->add_cert(anchor_creds, FALSE, ocsp);
	}

	lib->credmgr->add_set(lib->credmgr, &anchor_creds->set);

	/* Standalone validator instances for direct invocation. */
	constraints_inst = constraints_validator_create();
	if (constraints_inst)
	{
		constraints_v = &constraints_inst->validator;
	}
	revocation_inst = revocation_validator_create();
	if (revocation_inst)
	{
		revocation_v = &revocation_inst->validator;
	}
	return 0;
}

/**
 * Try to parse intermediate certs from the mutator buffer, */
static certificate_t *parse_cert_chain(const uint8_t *p, size_t remaining,
									   mem_cred_t *creds)
{
	certificate_t *cert;
	chunk_t blob;
	uint16_t clen;
	int n = 0;

	while (n < MAX_INTERMEDIATES && remaining >= 3)
	{
		clen = ((uint16_t)p[0] << 8) | p[1];
		if (clen == 0 || (size_t)clen + 2 >= remaining)
		{
			break;
		}
		blob = chunk_create((u_char*)p + 2, clen);
		cert = lib->creds->create(lib->creds, CRED_CERTIFICATE, CERT_X509,
								   BUILD_BLOB, blob, BUILD_END);
		if (cert)
		{
			creds->add_cert(creds, FALSE, cert);
			n++;
		}
		p += 2 + clen;
		remaining -= 2 + clen;
	}

	if (remaining == 0)
	{
		return NULL;
	}
	return lib->creds->create(lib->creds, CRED_CERTIFICATE, CERT_X509,
							   BUILD_BLOB, chunk_create((u_char*)p, remaining),
							   BUILD_END);
}

int LLVMFuzzerTestOneInput(const uint8_t *buf, size_t len)
{
	mem_cred_t *iter_creds;
	certificate_t *subject;
	certificate_t *enum_cert;
	auth_cfg_t *enum_auth;
	public_key_t *enum_key;
	enumerator_t *enumerator;
	identification_t *peer_id;
	bool online;
	int id_idx;

	/* 1 byte selector (online flag + peer-id index) + cert bytes. */
	if (len < 2)
	{
		return 0;
	}

	online = buf[0] & 1;
	id_idx = (buf[0] >> 1) % countof(peer_ids);

	iter_creds = mem_cred_create();
	subject = parse_cert_chain(buf + 1, len - 1, iter_creds);
	if (!subject)
	{
		iter_creds->destroy(iter_creds);
		return 0;
	}
	iter_creds->add_cert(iter_creds, FALSE, subject);

	lib->credmgr->add_local_set(lib->credmgr, &iter_creds->set, FALSE);

	peer_id = identification_create_from_string((char*)peer_ids[id_idx]);

	/* create_trusted_enumerator triggers verify_trust_chain + every registered validator. */
	enumerator = lib->credmgr->create_trusted_enumerator(lib->credmgr,
														   KEY_ANY, peer_id,
														   online);
	if (enumerator)
	{
		while (enumerator->enumerate(enumerator, &enum_cert, &enum_auth))
		{
			/* No-op enumeration */
		}
		enumerator->destroy(enumerator);
	}

	/* create_public_enumerator additionally exercises pubkey extraction + auth_cfg merging. */
	enumerator = lib->credmgr->create_public_enumerator(lib->credmgr,
														  KEY_ANY, peer_id,
														  NULL, online);
	if (enumerator)
	{
		while (enumerator->enumerate(enumerator, &enum_key, &enum_auth))
		{
			/* No-op enumeration */
		}
		enumerator->destroy(enumerator);
	}

	/* Direct validator invocation */
	if (anchor_cert && (constraints_v || revocation_v))
	{
		auth_cfg_t *direct_auth = auth_cfg_create();
		if (constraints_v)
		{
			if (constraints_v->validate)
			{
				constraints_v->validate(constraints_v, subject, anchor_cert,
										0, FALSE, direct_auth);
			}
			if (constraints_v->check_lifetime)
			{
				constraints_v->check_lifetime(constraints_v, subject,
											  0, FALSE, direct_auth);
			}
		}
		if (revocation_v)
		{
			if (revocation_v->validate)
			{
				revocation_v->validate(revocation_v, subject, anchor_cert,
									   0, FALSE, direct_auth);
			}
			if (online && revocation_v->validate_online)
			{
				revocation_v->validate_online(revocation_v, subject,
											  anchor_cert, 0, FALSE,
											  direct_auth);
			}
		}
		direct_auth->destroy(direct_auth);
	}

	peer_id->destroy(peer_id);
	lib->credmgr->remove_local_set(lib->credmgr, &iter_creds->set);
	iter_creds->destroy(iter_creds);
	return 0;
}
